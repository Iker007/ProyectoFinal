package clases;

public class Tipo {
	private String nombre;
	
	
	
	public Tipo() {
		super();
	}


	public Tipo(String nombre) {
		super();
		this.nombre = nombre;
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
